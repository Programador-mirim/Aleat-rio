# Conjunto vazio (observe que não podemos usar {} que cria um dicionário vazio)
conjunto_vazio = set()
# Conjunto com elementos
frutas = {'maçã', 'banana', 'laranja', 'uva'}
# Criando conjunto a partir de uma lista (remove duplicatas automaticamente)
numeros = set([1, 2, 3, 2, 4, 1, 5]) # {1, 2, 3, 4, 5}
# Conjunto a partir de uma string (cada caractere se torna um elemento)
letras = set('abracadabra') # {'a', 'b', 'c', 'd', 'r'}